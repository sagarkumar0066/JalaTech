package loops;

public class inloops1 
{
	public static void main(String args[])  // i covered 1,2 and 7 programs in loops sections 
	{
		for(int i=1;i<=10;i++)  // for loop
		{
			System.out.println("Bright IT Career");
		}
		
		int j=0;
		while(j++ <= 20)      // while loop           //  i am trying to cover two or more programs in one class to reduce time and space..
		{    
			if(j != 21)
			System.out.print(j+" ");
		}
		
		System.out.println();
		
		int k=1;
		do                    //do while loop
		{
			System.out.print(k+" ");
			
			k++;
		}
		while(k<=10);
	}
}
