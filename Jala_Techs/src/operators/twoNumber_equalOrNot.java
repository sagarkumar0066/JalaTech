package operators;

public class twoNumber_equalOrNot 
{
	public static void main(String args[])
	   {
		  int m=15,n=11;
		  
		  if(m==n)
		  {
			  System.out.println("Two numbers are equal");
		  }
		  else if(m!=n)
		  {
			  System.out.println("Two numbers are not equal");
		  }
	   }
}
